#include "libft.h"

void	ft_bzero(void	*s, size_t	n)
{
	size_t			i;
	unsigned char	*s_new;

	i = 0;
	s_new = s;
	if (s)
	{
		while (i < n)
		{
			s_new[i] = '\0';
			++i;
		}
	}
}
